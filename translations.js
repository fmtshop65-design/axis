function getTranslation(lang, key) {
  return translations[lang]?.[key] || translations['en']?.[key] || key;
}

function setLanguage(lang) {
  localStorage.setItem('axisLanguage', lang);
  location.reload();
}

function getCurrentLanguage() {
  return localStorage.getItem('axisLanguage') || 'en';
}

function updatePageText(lang) {
  // Plain text nodes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = getTranslation(lang, el.dataset.i18n);
  });
  // Rich text that may contain markup like <br>
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    el.innerHTML = getTranslation(lang, el.getAttribute('data-i18n-html'));
  });
  // Form field placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.setAttribute('placeholder', getTranslation(lang, el.getAttribute('data-i18n-placeholder')));
  });
}

document.addEventListener('DOMContentLoaded', () => {
  const lang = getCurrentLanguage();
  updatePageText(lang);
  const sel = document.querySelector('[data-lang-select]');
  if (sel) sel.value = lang;
});
